// 비어 있으면 유튜브 검색 링크로 자동 폴백합니다.

// 축의 "방식"은 셋 중 하나로 상호 배타적. 저소음은 그 위에 덧붙는 별개 속성.
export type SwitchType = "linear" | "tactile" | "clicky";

export interface Keyswitch {
  slug: string;
  nameKo: string;
  /** 영문명 (있으면) */
  nameEn?: string;
  brand: string;
  /** 축 방식 (리니어/택타일/클릭키 중 하나) */
  type: SwitchType;
  /** 저소음(댐퍼) 여부 — 예: 리니어 + 저소음 */
  silent?: boolean;
  /** 자석축(홀이펙트, 우팅호환) 여부 — 리니어 위에 덧붙는 별개 속성 */
  magnetic?: boolean;
  /** 대표 색상 표시 이름 */
  color?: string;
  /** 대표 색상 HEX (배지/칩 표시에 사용) */
  colorHex?: string;
  /** 작동 압력 (g) */
  actuationForce?: number;
  /** 바닥압 (g) */
  bottomOutForce?: number;
  /** 총 이동거리 (mm) */
  totalTravel?: number;
  /** 소리 성향 한줄 */
  soundProfile?: string;
  /** 키감 한줄 요약 */
  feelSummary?: string;
  /** 상세 설명 */
  description?: string;
  /** 검증된 타건음 유튜브 영상 ID (비면 검색 링크로 폴백) */
  youtubeVideoIds?: string[];
  /** 인스타그램 해시태그 (한/영) */
  instagramTags: string[];
  /** 출처 표시 — 스토어 판매 목록 기반이면 'store' */
  source?: "store";
  /** 공개 자료가 적어 스펙이 부분적/추정인 경우 */
  needsInfo?: boolean;
}

